package com.OOPSREVISION.com;

public class Single_Inheritance {
	
	public void chennai() {

		System.out.println("Chennai: MARINA BEACH");
	}
	
	private void thanjavur() {

		System.out.println("Thanjai: BIG TEMPLE");
		
	}

	protected void ooty() {

		System.out.println("OOTY: QUEEN OF HILLSTATION");
	}
	public static void main(String[] args) {
		
		Single_Inheritance data = new Single_Inheritance();
		
		data.chennai();
		data.thanjavur();
		data.ooty();
		
		
		
		
	}
}
