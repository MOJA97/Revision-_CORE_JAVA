package com.javarevision2.com;

public class While_Loop {

	
	public static void main(String[] args) {
		
		int i =2;
		while (i<=5) {
			System.out.println(i);
			i++;
			
		}
		System.out.println("**************************************************");
		//initialization;
		//while(CONDITION);
		//Implementation part;
		//Iteration
		
		int j = 5;
		do {
			System.out.println(j);
			j--;
		} while (j>=0);
		System.out.println("Do while");
	}
	
	
}
