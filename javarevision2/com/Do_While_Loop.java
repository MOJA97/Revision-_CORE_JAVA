package com.javarevision2.com;

public class Do_While_Loop {

	public static void main(String[] args) {
		
		int k = 0;
		do {
			System.out.println(k);
			k++;
			
		} while (k<=5);
		
		System.out.println("__________________________________________________");
		
		int l = 19;
		do {
			System.out.println(l);
			l--;
		} while (l>=10);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
}
