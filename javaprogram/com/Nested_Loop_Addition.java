package com.javaprogram.com;


public class Nested_Loop_Addition {
	
	public static void main(String[] args) {
		
		
			
		
		//outer Loop
		for (int i = 1; i <=5; i++) {
			//i = 1 i <=5 = true;
//			System.out.println(i+ " ");
			//inner loop for number
			for (int j = 1; j<=i; j++) {
//				System.out.println(i+" ");
				System.out.print(j+" "); 
								
			}
			System.out.println("");
			
			
		}
		
		
		
		
		
		
	}

}
